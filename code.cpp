void solve(){
    int n;
    cin >> n;
    long long total = 1LL * n * (n + 1) / 2;
    if (total % 2) {
        cout << 0 << "\n";
        return;
    }

    int sum = total / 2;
    vector<int> dp(sum + 1, 0);
    dp[0] = 1;

    for (int ele = 1; ele <= n; ele++) {
        for (int x = sum; x >= ele; --x) {
            dp[x] = (dp[x] + dp[x - ele]) % MOD;
        }
    }

    debug(dp);
    cout << (dp[sum] * ((MOD + 1) / 2)) % MOD << "\n"; 
}


